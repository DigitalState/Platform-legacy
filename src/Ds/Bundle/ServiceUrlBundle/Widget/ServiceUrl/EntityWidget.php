<?php

namespace Ds\Bundle\ServiceUrlBundle\Widget\ServiceUrl;

use Ds\Bundle\WidgetBundle\Widget\Widget;

/**
 * Class EntityWidget
 */
class EntityWidget extends Widget
{
    /**
     * {@inheritdoc}
     */
    public function getTitle()
    {
        return 'ds.serviceurl.widget.entity';
    }

    /**
     * {@inheritdoc}
     */
    public function getContent(array $data = [])
    {
        return $this->templating->render('@DsServiceUrlBundle/Resources/views/ServiceUrl/widget/entity.html.twig', $data);
    }
}
