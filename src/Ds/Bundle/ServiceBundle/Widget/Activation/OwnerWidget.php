<?php

namespace Ds\Bundle\ServiceBundle\Widget\Activation;

use Ds\Bundle\WidgetBundle\Widget\Widget;

/**
 * Class OwnerWidget
 */
class OwnerWidget extends Widget
{
    /**
     * {@inheritdoc}
     */
    public function getTitle()
    {
        return 'ds.service.activation.widget.owner';
    }

    /**
     * {@inheritdoc}
     */
    public function getContent(array $data = [])
    {
        return $this->templating->render('@DsServiceBundle/Resources/views/Activation/widget/owner.html.twig', $data);
    }
}
